    private void getRoomsBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_getRoomsBtnActionPerformed
        // TODO add your handling code here:
        BackBtn.setEnabled(true);
        int row = -1;
        row = jTable3.getSelectedRow();
        if(row == -1){
            JOptionPane.showMessageDialog(this, "you didn't choose hotel");
        }else{
            String hotelId = jTable3.getModel().getValueAt(row, 0).toString();
            Object [] col = {"Room Number", "Room Price", "Room Type", "Maximum11 Guests"};
            System.out.println("Hey you there"+row);
            DefaultTableModel Table = new DefaultTableModel();
            Table.setColumnIdentifiers(col);
            for (Hotel hotel: hotels) {
                if(hotel.getId().equals(hotelId)){
                    newBook.setCus(cus);
                    newBook.setHotel(hotel);
                    getRoomsBtn.setEnabled(false);
                    BookBtn.setEnabled(true);
                    List<Room> rooms = hotel.getRooms();
                    for(Room room : rooms){
                        Object [] tempRow = {room.getRoomNum(), room.getPrice(), room.getRoomType(), room.getMaxGuest()};
                        Table.addRow(tempRow);
                    }
                }
            }
            
            jTable3.setModel(Table);
            
        }
        
        
        
    }//GEN-LAST:event_getRoomsBtnActionPerformed

