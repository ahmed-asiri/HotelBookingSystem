    private void SearchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchBtnActionPerformed
        BookBtn.setEnabled(false);
        Date in = checkInDateForm.getDate();
        Date out = checkOutDateForm.getDate();
        newBook.setIn(in);
        newBook.setOut(out);
        int index = CityComboBox.getSelectedIndex();
        String city;
            if(in != null && out != null && index != -1 ){
                city = CityComboBox.getItemAt(index);
                if(in.compareTo(out) == 0 || in.compareTo(out) > 0){
                    JOptionPane.showMessageDialog(this, "Wrong date dimensions");
                }else{
                    
                    try {
                        
                        Object [] col = {"Hotel ID", "Hotel Name", "City", "Hotel Phone", "Hotel Email"};
                        
                        DefaultTableModel Table = new DefaultTableModel();
                        Table.setColumnIdentifiers(col);
                        hotels = HotelSearch.Search(in, out, city);
                        if(hotels != null){
                            getRoomsBtn.setEnabled(true);
                            for(Hotel hotel: hotels){
                                Object [] info = {hotel.getId() , hotel.getName(), hotel.getAddress().getCity(), hotel.getPhone(), hotel.getEmail()};
                                Table.addRow(info);
                                }
                            
                        }
                        jTable3.setModel(Table);
                        //jTable3.setModel(DbUtils.resultSetToTableModel(rs));
                    } catch (SQLException ex) {
                        Logger.getLogger(Search.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }else{
                JOptionPane.showMessageDialog(this,"There is missing input");
            }

    }//GEN-LAST:event_SearchBtnActionPerformed

